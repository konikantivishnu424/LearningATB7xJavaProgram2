package ex_13072024;

public class Lab041 {
    public static void main(String[] args) {
        //Compound Assignment operators
        int a =10;
        //+=, -=,*=,/=
        //+= ---->
        a+= 10;  // a= a+10;
        a-=10;   // a= a-10;
        a*=10;  // a= a*10;
        a/=10;   //a =a/10;
        System.out.println();
    }
}
